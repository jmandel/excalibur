#!/usr/bin/env python3
from __future__ import annotations
import argparse, os, shutil, subprocess, sys, tarfile, urllib.request
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
WORK = ROOT / 'experiments/static-wasi-python'
SRC = WORK / 'Python-3.12.0'
TGZ = WORK / 'Python-3.12.0.tgz'
PY_URL = 'https://www.python.org/ftp/python/3.12.0/Python-3.12.0.tgz'
WASMTIME = ROOT / 'experiments/wasi-python-spike/wasmtime/wasmtime-v18.0.4-x86_64-linux/wasmtime'
WASI_SDK = Path.home() / '.local/share/wasmpy-build/wasi-sdk'
THIRD_PARTY_PREFIX = WORK / 'third-party-wasi'
THIRD_PARTY_SRC = WORK / 'third-party-src'
THIRD_PARTY_BUILD = WORK / 'third-party-build'
THIRD_PARTY_SITE = WORK / 'third-party-site'
REGEX_TGZ = THIRD_PARTY_SRC / 'regex-2024.11.6.tar.gz'
REGEX_URL = 'https://files.pythonhosted.org/packages/source/r/regex/regex-2024.11.6.tar.gz'
MSGPACK_TGZ = THIRD_PARTY_SRC / 'msgpack-1.1.1.tar.gz'
MSGPACK_URL = 'https://files.pythonhosted.org/packages/source/m/msgpack/msgpack-1.1.1.tar.gz'
LXML_TGZ = THIRD_PARTY_SRC / 'lxml-5.3.0.tar.gz'
LXML_URL = 'https://files.pythonhosted.org/packages/source/l/lxml/lxml-5.3.0.tar.gz'

SETUP_LOCAL_HEADER = '''# Generated by scripts/static_wasi/build_static_wasi_python.py.
# WASI target policy: no dynamic extension modules.  The Android/Chicory path
# needs one self-contained python.wasm, so third-party C extensions are added
# here as builtin/static modules instead of .so/.wasm extension files.
#
# CPython 3.12's wasm32-wasi build already makes stdlib extension modules
# static by default.  These test/limited-ABI modules are marked shared in
# Modules/Setup.stdlib.in; disable them so `make all` never invokes wasm-ld as
# a dynamic-extension linker.
*disabled*
_testimportmultiple
_testmultiphase
_testsinglephase
xxlimited
xxlimited_35
'''.rstrip() + '\n'

def run(cmd, *, cwd=None, env=None, check=True):
    print('+', ' '.join(map(str, cmd)))
    return subprocess.run(list(map(str, cmd)), cwd=cwd, env=env, check=check)

def ensure_source():
    WORK.mkdir(parents=True, exist_ok=True)
    if not TGZ.exists():
        urllib.request.urlretrieve(PY_URL, TGZ)
    if not SRC.exists():
        with tarfile.open(TGZ) as t:
            t.extractall(WORK)

def ensure_wasmtime():
    if WASMTIME.exists():
        return
    run([sys.executable, str(ROOT / 'experiments/wasi_python_probe.py')], check=False)
    if not WASMTIME.exists():
        raise SystemExit(f'wasmtime not found at {WASMTIME}')

def regex_source_dir() -> Path:
    return THIRD_PARTY_BUILD / 'regex-2024.11.6'

def msgpack_source_dir() -> Path:
    return THIRD_PARTY_BUILD / 'msgpack-1.1.1'

def lxml_source_dir() -> Path:
    return THIRD_PARTY_BUILD / 'lxml-5.3.0'

def ensure_lxml_source():
    THIRD_PARTY_SRC.mkdir(parents=True, exist_ok=True)
    THIRD_PARTY_BUILD.mkdir(parents=True, exist_ok=True)
    if not LXML_TGZ.exists():
        urllib.request.urlretrieve(LXML_URL, LXML_TGZ)
    dest = lxml_source_dir()
    if not dest.exists():
        with tarfile.open(LXML_TGZ) as t:
            t.extractall(THIRD_PARTY_BUILD)

def ensure_msgpack_source():
    THIRD_PARTY_SRC.mkdir(parents=True, exist_ok=True)
    THIRD_PARTY_BUILD.mkdir(parents=True, exist_ok=True)
    if not MSGPACK_TGZ.exists():
        urllib.request.urlretrieve(MSGPACK_URL, MSGPACK_TGZ)
    dest = msgpack_source_dir()
    if not dest.exists():
        with tarfile.open(MSGPACK_TGZ) as t:
            t.extractall(THIRD_PARTY_BUILD)

def ensure_regex_source():
    THIRD_PARTY_SRC.mkdir(parents=True, exist_ok=True)
    THIRD_PARTY_BUILD.mkdir(parents=True, exist_ok=True)
    if not REGEX_TGZ.exists():
        urllib.request.urlretrieve(REGEX_URL, REGEX_TGZ)
    dest = regex_source_dir()
    if not dest.exists():
        with tarfile.open(REGEX_TGZ) as t:
            t.extractall(THIRD_PARTY_BUILD)


def stage_lxml_package():
    src = lxml_source_dir() / 'src/lxml'
    dest = THIRD_PARTY_SITE / 'lxml'
    if dest.exists():
        shutil.rmtree(dest)
    dest.parent.mkdir(parents=True, exist_ok=True)
    shutil.copytree(src, dest, ignore=shutil.ignore_patterns('*.c', '*.h', '*.pxd', '*.pxi', 'tests', '__pycache__'))
    aliases = {
        'etree': '_lxml_etree',
        'objectify': '_lxml_objectify',
        '_elementpath': '_lxml_elementpath',
        'builder': '_lxml_builder',
        'sax': '_lxml_sax',
    }
    for name, builtin in aliases.items():
        (dest / f'{name}.py').write_text(
            f"import sys as _sys\nimport {builtin} as _m\n_sys.modules[__name__] = _m\n"
        )
    init_names = {
        'etree.c': ('wasi_lxml_etree.c', 'PyInit_etree', 'PyInit__lxml_etree'),
        'objectify.c': ('wasi_lxml_objectify.c', 'PyInit_objectify', 'PyInit__lxml_objectify'),
        '_elementpath.c': ('wasi_lxml_elementpath.c', 'PyInit__elementpath', 'PyInit__lxml_elementpath'),
        'builder.c': ('wasi_lxml_builder.c', 'PyInit_builder', 'PyInit__lxml_builder'),
        'sax.c': ('wasi_lxml_sax.c', 'PyInit_sax', 'PyInit__lxml_sax'),
    }
    for source, (staged, old_init, new_init) in init_names.items():
        out = SRC / f'Modules/{staged}'
        shutil.copy2(src / source, out)
        out.write_text(out.read_text().replace(old_init, new_init))

def stage_msgpack_package():
    src = msgpack_source_dir() / 'msgpack'
    dest = THIRD_PARTY_SITE / 'msgpack'
    if dest.exists():
        shutil.rmtree(dest)
    dest.parent.mkdir(parents=True, exist_ok=True)
    shutil.copytree(src, dest, ignore=shutil.ignore_patterns('*.c', '*.h', '*.pyx', '__pycache__'))
    init = dest / '__init__.py'
    init.write_text(init.read_text().replace('from ._cmsgpack import Packer, Unpacker, unpackb', 'from _cmsgpack import Packer, Unpacker, unpackb'))
    shutil.copy2(src / '_cmsgpack.c', SRC / 'Modules/wasi_cmsgpack.c')

def stage_regex_package():
    src = regex_source_dir() / 'regex_3'
    dest = THIRD_PARTY_SITE / 'regex'
    if dest.exists():
        shutil.rmtree(dest)
    dest.parent.mkdir(parents=True, exist_ok=True)
    shutil.copytree(src, dest, ignore=shutil.ignore_patterns('*.c', '*.h', 'test_regex.py', '__pycache__'))
    core = dest / '_regex_core.py'
    core.write_text(core.read_text().replace('import regex._regex as _regex', 'import _regex'))
    regex_py = dest / 'regex.py'
    regex_py.write_text(regex_py.read_text().replace('import regex._regex as _regex', 'import _regex'))
    # makesetup derives object paths from source basenames and does not handle
    # absolute source paths well. Copy C sources into CPython's Modules dir with
    # flat unique names, while retaining the original include dir for headers.
    shutil.copy2(src / '_regex.c', SRC / 'Modules/wasi_regex.c')
    shutil.copy2(src / '_regex_unicode.c', SRC / 'Modules/wasi_regex_unicode.c')

def env():
    if not (WASI_SDK / 'bin/clang').exists():
        raise SystemExit(f'WASI SDK not found at {WASI_SDK}; install wasmpy-build first')
    e = os.environ.copy()
    e['WASI_SDK_PATH'] = str(WASI_SDK)
    e['PATH'] = f'{WASMTIME.parent}:{WASI_SDK / "bin"}:{e["PATH"]}'
    return e

def setup_local_text():
    text = SETUP_LOCAL_HEADER
    rsrc = regex_source_dir() / 'regex_3'
    if (rsrc / '_regex.c').exists():
        text += '\n# Third-party native modules built as top-level builtins. Package Python\n'
        text += '# files in third-party-site patch dotted imports to these names.\n'
        # Start a new static section after the disabled block; otherwise makesetup
        # treats following module lines as disabled too.
        text += '*static*\n'
        text += f'_regex wasi_regex.c wasi_regex_unicode.c -I{rsrc}\n'
    msrc = msgpack_source_dir() / 'msgpack'
    if (msrc / '_cmsgpack.c').exists():
        if '*static*' not in text.split('xxlimited_35', 1)[-1]:
            text += '*static*\n'
        text += f'_cmsgpack wasi_cmsgpack.c -I{msrc} -I{msrc.parent}\n'
    lsrc = lxml_source_dir() / 'src/lxml'
    if (lsrc / 'etree.c').exists():
        if '*static*' not in text.split('xxlimited_35', 1)[-1]:
            text += '*static*\n'
        incs = f'-I{lsrc} -I{lsrc / "includes"} -I{THIRD_PARTY_PREFIX / "include"} -I{THIRD_PARTY_PREFIX / "include/libxml2"}'
        libs = f'{THIRD_PARTY_PREFIX / "lib/libexslt.a"} {THIRD_PARTY_PREFIX / "lib/libxslt.a"} {THIRD_PARTY_PREFIX / "lib/libxml2.a"}'
        text += f'_lxml_etree wasi_lxml_etree.c {incs} {libs}\n'
        text += f'_lxml_objectify wasi_lxml_objectify.c {incs} {libs}\n'
        text += f'_lxml_elementpath wasi_lxml_elementpath.c {incs}\n'
        text += f'_lxml_builder wasi_lxml_builder.c {incs} {libs}\n'
        text += f'_lxml_sax wasi_lxml_sax.c {incs} {libs}\n'
    return text

def write_setup_local():
    (SRC / 'Modules/Setup.local').write_text(setup_local_text())
    bcopy = SRC / 'builddir/wasi/Modules/Setup.local'
    if bcopy.parent.exists():
        bcopy.write_text(setup_local_text())

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument('--clean', action='store_true', help='clean CPython wasm build before building')
    args = ap.parse_args()
    ensure_source(); ensure_wasmtime(); ensure_regex_source(); ensure_msgpack_source(); ensure_lxml_source(); stage_regex_package(); stage_msgpack_package(); stage_lxml_package(); write_setup_local()
    base_env = env()
    host_cmd = [sys.executable, './Tools/wasm/wasm_build.py']
    if args.clean:
        host_cmd.append('--clean')
    host_cmd += ['build', 'build']
    run(host_cmd, cwd=SRC, env=base_env)

    target_env = base_env.copy()
    if THIRD_PARTY_PREFIX.exists():
        inc = THIRD_PARTY_PREFIX / 'include'; lib = THIRD_PARTY_PREFIX / 'lib'; pc = lib / 'pkgconfig'
        target_env['CPPFLAGS'] = f'-I{inc} ' + target_env.get('CPPFLAGS', '')
        target_env['LDFLAGS'] = f'-L{lib} ' + target_env.get('LDFLAGS', '')
        target_env['PKG_CONFIG_PATH'] = str(pc)
    # lxml's generated etree.c is enormous; compiling it at CPython's default
    # -O3 exhausts this VM. Keep target C extension optimization modest.
    target_env['CFLAGS'] = '-O0 -g0 ' + target_env.get('CFLAGS', '')
    target_env['MAKEFLAGS'] = '-j1'
    if args.clean:
        shutil.rmtree(SRC / 'builddir/wasi', ignore_errors=True)
    run([sys.executable, './Tools/wasm/wasm_build.py', 'wasi', 'configure'], cwd=SRC, env=target_env)
    write_setup_local()
    run([sys.executable, './Tools/wasm/wasm_build.py', '--clean', 'wasi', 'compile'], cwd=SRC, env=target_env)
    print(SRC / 'builddir/wasi/python.wasm')

if __name__ == '__main__':
    main()
